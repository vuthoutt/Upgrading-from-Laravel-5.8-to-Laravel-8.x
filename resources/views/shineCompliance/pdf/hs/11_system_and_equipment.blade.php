        <h3 class="mt30 mb30"><strong>The Health & Safety at Work Act 1974</strong></h3>
        <div class="mt30">
            <p>
                The basis of health and safety legislation relating to the workplace is the <strong>Health and Safety at Work etc Act 1974</strong>. It is also known as <strong>HASAWA</strong> or <strong>HSW</strong> and most health and safety legislation is contained in it. HASAW is based on common sense and safe practice.
                It places the duty on employers to take responsibility for the health and safety of their employees at work “as far as is reasonably practicable”.
            </p>
        </div>
        <p class="mt30">
            <strong>The responsibilities of the employer:</strong>
        </p>
        <ul style="margin-left: 5px">
            <li>Provide and maintain safe systems of work</li>
            <li>Provide adequate health and safety induction and training</li>
            <li>Maintain safe work equipment</li>
            <li>Ensure safe operation of working equipment</li>
            <li>Ensure adequate welfare provisions are made</li>
            <li>Provide a safe place of work. This includes safe access and egress</li>
            <li>Ensure any materials are handled, transported, used and stored safely</li>
            <li>Communicate with safety representatives</li>
            <li>Provide PPE or any other equipment needed in the interests of health and safety</li>
        </ul>
        <p class="mt30">
            <strong>Do employees have any responsibilities?</strong>
        </p>
        <p class="mt30">
            Yes, there are responsibilities for the employee too.  Employees must:
        </p>
        <ul style="margin-left: 5px">
            <li>Take care of their own health and safety and that of others</li>
            <li>Not interfere with any health hand safety equipment</li>
            <li>Cooperate with employers</li>
        </ul>
        <h3 class="mt30">
            Management of Health and Safety at Work Regulations 1999
        </h3>
        <p class="mt30">
            Also known as the ‘Management Regs’, the <strong>Management of Health and Safety at Work Regulations 1999</strong> place a duty on employers to assess and manage risk.
            Specifically, they require employers to do the following:
        </p>
        <ul style="margin-left: 5px">
            <li>Manage risk in the workplace by carrying out risk assessments.</li>
            <li>Take action to reduce or eliminate risks.</li>
            <li>Appoint a ‘competent person’ to oversee health and safety in the workplace.</li>
            <li>Provide staff with information and training with regards to safe working practice.</li>
            <li>Have a health and safety policy in place.</li>
        </ul>
    </div>
