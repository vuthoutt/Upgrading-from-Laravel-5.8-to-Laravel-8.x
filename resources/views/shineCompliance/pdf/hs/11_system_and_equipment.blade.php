        <h3 class="mt30">
            Health and Safety Risk Assessment
        </h3>
        <div class="mt30">
            <p>
                A Health and Safety Assessment is an organised and methodical look at your premises, the individual activities undertaken,
                and the likelihood that a health and safety incident could cause harm to those in and around the premises.
                It should be available for inspection or validation by any authorised person at all times.
            </p>
        </div>
        <h3 class="mt30">
            Aims
        </h3>
        <div class="mt30">
            <p>
                The aims of the Health and Safety Assessment are to:
            </p>
        </div>
        <ul style="margin-left: 5px">
            <li>Identify health and safety hazards.</li>
            <li>Reduce the risk of those hazards causing harm (to as low as reasonably practicable).</li>
            <li>Decide what precautions and management arrangements are necessary to ensure the safety of all occupants.</li>
        </ul>
        <div class="mt30">
            <p>
                The health and safety precautions that must be provided (and maintained) are those required to reasonably protect relevant persons from risks to themselves in the event of a fire.
                This will be determined by the findings contained within the assessment.
                The term Hazard refers to anything that has the potential to cause harm whilst Risk denotes the chance of that harm occurring.
            </p>
        </div>
        <h3 class="mt30">Specification</h3>
        <div class="mt30">
            <p>
                The assessment method employed shares many similarities with that used in general health and safety and the criteria for our judgement for compliance being:
            </p>
        </div>
        <ul style="margin-left: 5px">
            <li>Best Practice</li>
            <li>Building Regulations</li>
            <li>British Standards (relevant sections)</li>
        </ul>
        <p class="mt30">
            The assessment itself is conducted by way of a thorough and methodical visual inspection,
            the appraisal of relevant documented procedures and records and via consultation with appropriate stakeholders (where appropriate).
        </p>
        <h3 class="mt30">Responsible Person(s)</h3>
        <p class="mt30">
            The responsible person(s) (usually manager(s)) is a legally designated individual who has control, or a degree of control over work activities,
            over premises (or certain areas within) or prevention systems within these premises.
            Although this will usually be obvious, in shared premises or larger businesses, several people may share this responsibility.
        </p>
        <p class="mt30">
            In accordance with the Management of Health and Safety at Work Regulations 1999 and The Health & Safety at Work Act 1974,
            it is also the duty of the recognised responsible person(s) to provide the relevant individuals (particularly those most at risk) with comprehensive and relevant information,
            instruction and/or training, on the risks to them which have been identified as a result of this Health and Safety Assessment,
            together with any preventative and protective measures that are in place or are to be implemented, to deal with any such danger.
        </p>
